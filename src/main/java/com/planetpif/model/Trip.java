package com.planetpif.model;

public class Trip {

	private String from;
	private Integer id;
	private String to;

	public String getFrom() {
		return from;
	}

	public Integer getId() {
		return id;
	}

	public String getTo() {
		return to;
	}

	public void setFrom(String from) {
		this.from = from;
	}

	public void setId(Integer id) {
		this.id = id;
	}

	public void setTo(String to) {
		this.to = to;
	}
}
